import UIKit

extension UIColor {
    public static var primary: UIColor {
        return .white
    }
    public static var secondary: UIColor {
        return UIColor(red: 136/255, green: 0, blue: 40/255, alpha: 1)
    }
    public static var accent: UIColor {
        return UIColor(red: 1, green: 0, blue: 80/255, alpha: 1)
    }
    public static var background: UIColor {
        return UIColor(red: 50/255, green: 0, blue: 16/255, alpha: 1)
    }
}
